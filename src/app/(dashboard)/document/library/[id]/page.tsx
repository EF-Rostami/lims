"use client";

import { useEffect, useState } from "react";
import { internalDocumentService } from "@/services/internalDocument/internalDocument.service";
import { CheckCircle, Download, FileText, Printer } from "lucide-react";
import { Button } from "@/components/ui/button";

export default function LibraryDocumentView({ params }: { params: { id: string } }) {
  const [doc, setDoc] = useState<any>(null);
  const [read, setRead] = useState(false);

  useEffect(() => {
    internalDocumentService.getById(parseInt(params.id)).then(res => setDoc((res as any).data || res));
  }, [params.id]);

  if (!doc) return <div className="p-20 text-center">Loading controlled document...</div>;

  return (
    <div className="min-h-screen bg-white">
      {/* Static Compliance Header */}
      <div className="sticky top-0 z-10 bg-slate-900 text-white px-8 py-3 flex justify-between items-center print:hidden">
        <div className="flex flex-col">
          <span className="text-xs uppercase tracking-widest text-slate-400">Controlled Document</span>
          <span className="font-bold">{doc.title} ({doc.system_id})</span>
        </div>
        <div className="flex gap-3">
          <Button variant="outline" size="sm" className="text-white border-slate-700 hover:bg-slate-800" onClick={() => window.print()}>
            <Printer className="h-4 w-4 mr-2" /> Print
          </Button>
          <Button 
            disabled={read}
            onClick={() => { setRead(true); internalDocumentService.confirmRead(doc.id); }}
            className={read ? "bg-green-600" : "bg-blue-600 hover:bg-blue-700"}
          >
            {read ? <CheckCircle className="h-4 w-4 mr-2" /> : null}
            {read ? "Acknowledgement Recorded" : "Mark as Read"}
          </Button>
        </div>
      </div>

      <div className="max-w-4xl mx-auto p-12 shadow-sm my-8 border bg-white min-h-\[11in]\">
        {/* Content Rendering */}
        <div className="prose prose-slate max-w-none">
          <div className="text-center border-b-2 border-slate-900 pb-4 mb-8">
             <h1 className="m-0 uppercase">{doc.title}</h1>
             <p className="text-sm font-mono mt-2">ID: {doc.system_id} | VERSION: {doc.current_version_tag} | EFFECTIVE: {doc.release_date}</p>
          </div>
          
          {doc.versions?.[0]?.content ? (
            <div className="whitespace-pre-wrap">{doc.versions[0].content}</div>
          ) : (
             <div className="py-20 text-center border-2 border-dashed rounded-lg">
                <FileText className="h-12 w-12 mx-auto text-slate-300 mb-4" />
                <p>This is a file-based document. Please download to view.</p>
                <Button variant="link" className="mt-2 text-blue-600">Download Attachment</Button>
             </div>
          )}
        </div>
      </div>
      
      <footer className="text-center text-[10px] text-slate-400 pb-10 uppercase tracking-tighter print:block hidden">
        Controlled Document generated by LIMS on {new Date().toLocaleString()} - Uncontrolled if Printed
      </footer>
    </div>
  );
}